/** \brief Defines pin indexes for SVR
 */
typedef enum
{
    SVR1 = 1,             /** \brief SVR1 pin */
    SVR2 = 2,             /** \brief SVR2 pin */
} SVRPinsEnum;


/*********************************************************************************************************************/
/*------------------------------------------------Function Prototypes------------------------------------------------*/
/*********************************************************************************************************************/

/** \brief Default configuration of SVR pins (after POR of MCU)
 * Should be executed after POR
 * \return Returns nothing
 */
void InitSVRPins(void);

/** \brief Activate SVR pin (set pin to high level)
 * Should be configured as output first!
 * \param pinIdx SVR pin index as enum value
 * \return Returns nothing
 */
void SetSVRPin(SVRPinsEnum pinIdx);

/** \brief Deactivate SVR pin (set pin to low level)
 * Should be configured as output first!
 * \param pinIdx SVR pin index as enum value
 * \return Returns nothing
 */
void ClearSVRPin(SVRPinsEnum pinIdx);
void HandleFiringCommand(void);                     //Change
