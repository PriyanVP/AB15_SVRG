// SELECT definition
static IfxPort_Pin SELECT;

/*********************************************************************************************************************/
/*---------------------------------------------Function Implementations----------------------------------------------*/
/*********************************************************************************************************************/

// Default configuration of Select pin
void ConfigureSelectPin(void)
{
    // Init pin reference
    SELECT = IfxPort_P00_7;

    // Set as input with pull down (active high)
    IfxPort_setPinModeInput(SELECT.port, SELECT.pinIndex, IfxPort_InputMode_pullDown);
}