/** \brief Initialize UART interface.
 * This functions:\n
 * 1) Initializes pins for UART communication.\n
 * 2) Setups UART communication parameters.\n
 * 3) Creates interface for UART usage.\n
 *
 * \return Returns nothing.
 */
