/** \brief Send data via UART
 * Formats and sends data
 *
 * \param data pointer to buffer with data to send
 * \param count number of bytes to send
 * \return Returns TRUE if data could be written
 */
void SendUartData(const uint8 * data, Ifx_SizeT count);

