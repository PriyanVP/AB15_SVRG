uint16 ConstructWordFromBytes(uint8 msb, uint8 lsb)
{
    uint16 constructedWord = 0;
    constructedWord = lsb | (msb << 8);

    return constructedWord;
}

uint8 GetMSB(uint16 word)
{
    return ((word & 0xFF00) >> 8);
}

uint8 GetLSB(uint16 word)
{
    return (word & 0x00FF);
}

uint8 GetByteByIdx(uint8 byteIdx, uint32 word)
{
    // Input validation
    if (byteIdx > 3) return 0;

    uint32 mask = 0;
    uint8 bitOffset = (byteIdx * 8);

    mask = 0xFF << bitOffset;

    return (uint8)((word & mask) >> bitOffset);
}

uint8 SetResponseBit(uint8 msgID)
{
    return (msgID | 0x80);
}
