uint8 GetSpiChannelById(uint8 device_id)
{
    uint8 spiChannel;

    switch (device_id)
    {
        case 0:
            spiChannel = SPI_CH_INVALID;
            break;
        case 1:
            spiChannel = SPI1_CS1MASTER;
            break;
        case 2:
            // special case, mapped to master + pulls down SCMON1
            spiChannel = SPI1_CS_MON1;
            break;
        case 3:
            spiChannel = SPI1_CS1_SENSOR1;
            break;
        case 4:
            spiChannel = SPI1_CS1_SENSOR2;
            break;
        case 5:
            spiChannel = SPI1_CS1_SENSOR3;
            break;
        case 6:
            spiChannel = SPI2_CS2_SENSOR1;
            break;
        case 7:
            // special case, mapped to master + pulls down SCMON2
            spiChannel = SPI2_CS_MON2;
            break;
        case 8:
            spiChannel = SPI2_CS2_SENSOR2;
            break;
        case 9:
            spiChannel = SPI2_CS2_SLAVE1;
            break;
        case 10:
            spiChannel = SPI2_CS2_SLAVE2;
            break;
        case 11:
            spiChannel = SPI2_CS2_SLAVE3;
            break;
        case 12:
            spiChannel = SPI_CH_ENUM_LAST;
            break;
        default:
            spiChannel = SPI_CH_INVALID;
            break;
    }

    return spiChannel;
}
