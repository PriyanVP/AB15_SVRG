// Flag for MCU bypass softkey (disabling from GUI)
static boolean g_mcuBypass = FALSE;

/*********************************************************************************************************************/
/*---------------------------------------------Function Implementations----------------------------------------------*/
/*********************************************************************************************************************/

void CmdBypassMCU(USBReceiveData const * const commandPackage)
{
    // Set flag
    g_mcuBypass = TRUE;

    // Acknowlege GUI
    CmdIsAlive(commandPackage);
}

void CmdActivateMCU(USBReceiveData const * const commandPackage)
{
    // Set flag
    g_mcuBypass = FALSE;

    // Acknowlege GUI
    CmdIsAlive(commandPackage);
}

boolean GetMCUBypassFlag(void)
{
    return g_mcuBypass;
}